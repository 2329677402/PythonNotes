# demo:有两个变量，a 和 b，需求：将二者的值交换并打印结果
a = 30
b = 40
# ①使用中间变量来实现变量交换
print(f"未交换前 a={a},b={b}")  # 未交换前 a=30,b=40
temp = a
a = b
b = temp
print(f"交换后 a={a},b={b}")  # 交换后 a=40,b=30

# ②在Python中支持一个简单的实现变量交换方式：x,y = y,x
print(f"未交换前 a={a},b={b}")  # 未交换前 a=30,b=40
a, b = b, a
print(f"交换后 a={a},b={b}")  # 交换后 a=40,b=30

# ③不使用中间变量
print(f"未交换前 a={a},b={b}")  # 未交换前 a=30,b=40
a = a + b
b = a - b  # b = (a + b) - b = a
a = a - b  # a = (a + b) - a = b
print(f"交换后 a={a},b={b}")  # 交换后 a=40,b=30
